    $rootScope.paddingTop = 41;

    service.someState = false;

    service.getData = function(width, start, end) {
      //check start and stop time etc
      if(end === 0 && start === 0)
      {
        end = Math.round((Math.random() + 1) * width);
      }
      return getDestData(/* width, start , end */);
    };

